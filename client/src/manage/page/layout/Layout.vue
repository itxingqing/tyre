<script>
import _ from "lodash";
import MenuTree from "@manage/component/menutree/MenuTree";
import { menu } from "@manage/router";
import { mapGetters } from "vuex";

export default {
    name: "Layout",
    components: {
        MenuTree
    },

    data() {
        return {
            bodyHeight: 500,
            menu: menu,
            changePasswordVisable: false,

            changePwdForm: {
                oldPass: "",
                newPass: "",
                newPassRe: ""
            },

            rules: {
                passRequire: [
                    {
                        required: true,
                        message: "请填写所需要的密码",
                        trigger: "blur"
                    },
                    {
                        min: 6,
                        message: "长度最少6个字符",
                        trigger: "blur"
                    }
                ]
            },

            changLoading: false
        };
    },

    computed: {
        ...mapGetters(["userInfo"])
    },

    mounted() {
        const that = this,
            resize = () => {
                that.bodyHeight = that.$refs.mainBody.offsetHeight;
            };

        setTimeout(resize, 0);

        window.onresize = _.debounce(resize, 300);
    },

    methods: {
        changePassword() {
            let that = this;

            that.$refs[formName].validate(valid => {
                that.changLoading = true;

                that.$api.users
                    .changePassword(that.changePwdForm)
                    .then(res => {
                        if (res.error == 0) {
                            that.$message({
                                message: "修改成功.",
                                type: "success",
                                duration: 800
                            });                            

                            // that.changePwdForm.oldPass = "";
                            // that.changePwdForm.newPass = "";
                            // that.changePwdForm.newPassRe = "";

                            // this.$refs["changePwdForm"].resetFields();
                            that.resetForm();
                        } else {
                            that.$message.error(res.message);
                        }

                        that.changLoading = false;
                    })
                    .catch(res => {
                        that.$message.error("修改失败，请重试.");

                        that.changLoading = false;
                    });
            });
        },

        logout() {
            let that = this;

            that.$api.users
                .logout()
                .then(res => {
                    that.$store.commit("clearUserInfo");
                    that.$router.replace("/login");
                })
                .catch(res => {
                    that.$message.error("退出失败,请重试.");
                });
        },

        resetForm(){
            this.$refs["changePwdForm"].resetFields();

            that.changePasswordVisable = false;
        }
    }
};
</script>

